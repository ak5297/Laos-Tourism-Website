<div class="body-title">
<h1>Plain of Jars</h1>
</div>

<p>
If you want to see the Stonehenge's sister, you need to visit the Plain of Jars. Located in Xiangkhouang, these large
stone jars are made from rock ranging 3 to 10 feet tall and weigh up to 14 tons. The origins of the jars are 
unknown but archaeologists believe it was used 1,000 to 2,000 years ago. Researches believe that it was used for
funeral urns or food storage. It is also home to thousands of unexploded bombs from The Secret War that are scattered 
in the area. Tourists can visit 3 different sites that have already been cleared of the bombs. You can find 
tourists sitting on top of the jars while taking photos. Site 1 is the most popular being closest to the city and 
having a small museum. Site 2 has a hiking trail that leads through a forest. 
</p>

