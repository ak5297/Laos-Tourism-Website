<div class="body-title">
<h1>Pha That Luang Temple</h1>
</div>

<p>
Pha That Luang is perhaps the most elegant-looking monument in Vientiane. The architecture is influenced by 
Buddhism and has several small stupas. The whole temple is painted in gold and is built on 3 levels. The levels 
represent the distance from Earth to heaven. The first level signifies the underworld, second level is the 30 
perfections of Buddhism, and third level is the gates to heaven. Since it is a temple, tourists should be 
considerate in how they dress as to not offend the locals. It is the perfect place to just meditate or 
to learn about how Buddhism influenced Laos. 
</p>

