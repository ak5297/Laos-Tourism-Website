<div class="body-title">
<h1>Kuang Si Waterfalls</h1>
</div>

<p>Hidden inside the lush jungles of Luang Prabang is the Kuang Si Waterfalls. This landmark is extremely underrated
but is becoming overrun by tourists every year. The water is turquoise blue and clear making it a perfect place to
relax and take beautiful photos. The best spot to go is at the "secret pool" at the top of the waterfall
which overlooks the entire jungle. Also, there are ropes from trees that let you swing into the water. As you leave,
stop by the Tat Kuang Si Bear Rescue Center and admire the Asiatic Black Bears that have been rescued from 
dangerous conditions. The bears' habitat include hammocks, pools, and play areas to keep them entertained.</p>


