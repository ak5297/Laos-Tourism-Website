<h2>Luang Prabang</h2>
<p>
This small town encircled by mountains and greenery is called Luang Prabang, the former capital of Laos. In 1545, King Phothisarat moved
the administrative seat to Vientiane. The city was designated a UNESCO World Heritage city since 1995. This distinction was awarded because the city 
reflects the fusion of Lao and European architecture and the townscape is well preserved. 
While strolling around the city, you may notice that the buildings combines the traditional Lao wooden houses with some European architecture.
This influence goes back to when Laos was a French colony. It is a relatively small city with the Nam Khan and Mekong Rivers surrounding almost the 
entire city. You can find anything from former royal palaces to over 33 temples since the city remains the main center for Buddhist learning
in Laos. Thanks to its geographical location, it can be considered an adventurer's paradise. There are waterfalls, mountain peaks, and rivers 
providing endless opportunities to enjoy all Luang Prabang has to offer.
</p>

