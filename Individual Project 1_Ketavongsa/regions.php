<h2>Vientiane</h2>
<p>
The capital city, Vientiane, is located in this region. The city sits right on the border with Northern Thailand divided by the
Mekong River. It is known as one of the quietest capital cities in Southeast Asia. It is pronounced "Wiang Jan" and translates roughly to
the "City of Sandalwood". The city has a small-town atmosphere to it, and two days is enough to see sights around the city. There is a piece of history
around every corner, from the several temples such as Pha That Loung to the popular Patuxai monument. If you prefer nightlife, there are several 
bars and pubs in the downtown area. One of the most memorable experiences is shopping at the "talat" or market. There is the morning market open 
daily from 7AM-6PM. It is located inside the only mall in Laos. You can find street vendors that sell items from freshly baked baguettes to 
traditional Laotian clothing. From 6PM to 11PM, the night market is open by the riverside. This market is aimed towards tourists, and you can find 
souvenirs items such as Beer Lao t-shirts and Buddhist-inspired knickknacks. 
</p>

