<h2>Vang Vieng</h2>
<p>
About a 3 hour drive north of Vientiane, lies the beautiful riverside town of Vang Vieng. Known for its picturesque landscape and nightlife,
this town is a popular stop with backpackers. It is well known for tubing along the Mekong River, where you can float by restaurants and bars, 
referred to as "TV Bars", that play re-runs of Friends and Family Guy episodes. The majority of the bars have been shut down by the government 
due to "dangerous excessive partying" resulting in the death of 27 tourists. As of February 2016, there were just 2 bars still open. The town isn't
all about partying - it offers a vast amount of outdoor activities as well. There are tour companies that offer cave visits, kayaking, ziplines, 
swimming in water falls, and dirt buggies. 
</p>

