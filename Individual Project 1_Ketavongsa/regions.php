<h2>Si Phan Don</h2>

<p>
Would you have ever guessed that Laos has islands? Get your sunglasses and sunblock ready because the next location is Si Phan Don! 
The name literally means "4 Thousand Islands" in Laotian. While there are 4000 islands, only two of them are most visited: Don Dhet and Don Khong.
The rest are uninhabited due to their small size. Don Dhet is the most laid back island while Don Khong is the biggest and busiest island. You can find
Irrawaddy dolphin and plenty of fish in the water surrounding the islands. The dolphins are best spotted from the south of the islands
from December until May towards the end of the day. Boat tours are available as well. The islands are really secluded and there are only basic accommodations, so 
don't expect phones or electricity! Locals are self-sufficient and rely on local produce such as rice, fish, coconut, and sugar cane. Clothing is made
by their own weaving techniques. It really is an escape from the real world. 
</p>

