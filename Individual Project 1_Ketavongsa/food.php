<p>
Laotian food is unique and most dishes are served with sticky rice. While the popularity of dishes vary by region, there are a few
traditional platters that are enjoyed by almost every Laotian. 
These dishes include: 
</p>



<ul>
<li>Khao Niew: Sticky rice. Just roll it into a rice ball with your hand. It is a staple to eat sticky rice with your meal.</li>
<li><b>Padaek</b>: A fermented fish sauce made from anchovies used in many Lao dishes. Smelly but delicious.</li>
<li><b>Larb</b>: A salad made with minced meat flavored with fish sauce, lime juice, padaek, and fresh herbs. Typical meats used are beef, pork, duck, shrimp.</li>
<li><b>Kow Piek</b>: A soup made with noodles or rice. Made with chicken broth and topped with different meats and herbs. </li>
<li><b>Gang Nor Mai</b>: Shredded bamboo shoot soup. Made with lemongrass, fish sauce, chicken stock.</li> 
<li><b>Tam</b>: A salad made with vegetables, padaek, lime, and chili peppers. Typical vegetables are papaya, cucumber, green beans.</li>
</ul>

<h2>Essential Cooking Utensils</h2>
<ul>
<li><b>Tip Kow</b>: A bamboo made to store the sticky rice. </li>
<li><b>Kha Tok</b>: A mini table made out of rattan used to serve dishes when eating in a group.</li>
<li><b>Khok</b>: A mortar that is used for pounding food like Tam.</li>
</ul>

