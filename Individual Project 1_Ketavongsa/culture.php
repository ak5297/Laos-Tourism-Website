<div class="body-title">
<h1>Religion + Baci Ceremony</h1>
</div>

<p>
The majority of Laotians are Buddhists, this is evident by the Pha That Luang being the national symbol of the country. The Pha That Luang is a gilded
stupa temple located in Vientiane. There is also some influences of animism as well. There is a traditional ceremony called "baci" that is held
before significant events to bind the 32 guardian spirits to a person's body. Significant events can be a marriage, the birth of a child, 
a serious illness, or before leaving on a long trip. The baci ceremony is simply held to bring good luck and fortune to the person. 
</p>


<div class="body-title">
<h1>How is the baci ceremony celebrated?</h1>
</div>

<h2>Before The Ceremony</h2>

<p>
Elderly women in the community prepare the "Pha Khuan" which is a marigold pyramid that contains yellow/white flowers, 
white cotton threads, small candles, cooked sticky rice, boiled eggs and little treats. There is also "Maak Beng" which is folded banana leaves where 
small candles tied onto a stick is placed into. The white cotton threads are cut around 10 inches long, enough to tie on a person's wrist, and
tied onto sticks that are stuck into the Maak Beng. The Maak Beng is then placed onto a big round silver tray. Rice is put on the base of the tray 
to keep it stable and treats such as fruits and boiled eggs surround the Maak Beng. Finally, the finished Pha Khuan is placed on a white cloth
in the center of the room for the ceremony. The dress code is modest - women should wear the traditional "sinh" and men should wear a collared shirt
with long sleeve pants.
</p>

