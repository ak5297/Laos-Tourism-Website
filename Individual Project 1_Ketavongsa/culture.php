<h2>During The Ceremony</h2>
<p>
The baci is performed by an older male who is usually, or has been, a monk. Everyone sits on the floor facing the Pha Khuan while the person
performing the baci chants in an ancient Sanskrit language. While this is happening, everyone has their palms held together in a position that is 
called "nop". People that sit closer to the Pha Khuan may be holding the white strings from the Pha Khuan between their palms. Near the end of the 
ceremony, people that sit in the front reach forward to touch the Pha Khuan. Those that can't reach it put their hand on the person in front of them
to get some of the "luck". Finally, the white strings from the Pha Khuan are cut into small strips that are used to tie on people's wrists while 
wishing them well. In one hand, you hold onto a treat from the tray while the string is being tied. Afterwards, make sure you "nop" to 
show appreciation. The strings should be worn for at least 3 days. There is usually a party held afterwards, with music and plenty of food. 
It is expected that attendees stay for a little bit to socialize. 
</p>

