<div class="body-title">
<h1>Lao New Year</h1>
</div>

<p>
Lao New Year, or Bun Pi Mai, is celebrated annually in April and spans over three days. This occurs during the hot season and is a perfect time 
to visit the country. The festivities relate back to Buddhism and Animism with the belief that one's old spirit leaves this world, therefore
making room for a new one. On the first day of Pi Mai, it is considered the last day of the old year. Lao people clean their houses and prepare 
water, perfume, and flowers for the upcoming days. On the second day, it is considered a "day of no day" meaning it is not part of the old year
or part of the new year. The third day is when Lao New Year officially starts, locals will go "tak baht" which means giving alms to monks, go to
baci ceremonies, and soak each other in water! That's right, no matter where you go people will dump buckets of water or spray you with their 
water guns! 
</p>
