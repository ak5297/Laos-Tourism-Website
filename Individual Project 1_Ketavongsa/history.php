<div class="body-title">
<h1>The Early Days of Laos</h1>
</div>


<p> 
The history of Laos stretches back to 1353 when warlord Fa Ngum declared himself the king of Lan Xang, meaning "Million Elephants". 
The kingdom was a Khmer vassal state until 1694 when a succession dispute split it into three regions: Luang Prabang in the north, Vientiane 
in the middle and Champasak in the south. Eventually, the kingdom agreed to Siamese protection in 1885. 

East of the Mekong River, the area was taken from the Siam by the French who wanted a buffer state to protect Vietnam. In 1907, Laos was 
set up as a unified territory. In 1945, Japan briefly occupied Laos which triggered a conflict spanning over 30 years when the French wanted to 
reclaim its colony. Laos was granted its independence from France in 1953, but war continued between the communist Pathet Lao trying to overthrow 
the French-leaning monarchy. 
</p>

<div class="body-title">
<h1>The Rise of Communism</h1>
</div>

<p>
In 1950, Pro-Communist Prince Souphanouvong formed the organization "Pathet Lao", which means Land of the Lao. The country was divided with
parts of the country ruled by the royalist government backed by the USA. The other parts were ruled by the pro-Communist Pathet Lao with assistance
from their ally, the Viet Minh. In the 1960s, Laos was drawn into the Vietnam War when the USA bombed Pathet Lao territory but did not defeat 
them. In 1975, the Royalists fled Laos after seeing South Vietnam and Cambodia fall to the communists leading Pathet Lao to take over the entire country. 
After the fall of Saigon in 1975, the communist Pathet Lao took over the capital city, Vientiane, and ended a six-century-old monarchy. 
Closer ties to Vietnam and socialization were replaced with a steady return to private enterprise, easing of foreign investment laws, and
admission into ASEAN in 1997.
</p>

<div class="body-title">
<h1>Where is Laos Today?</h1>
</div>

<p>
Life in Laos has relatively stayed the same for hundred of years. Until now, things are starting to slowly change. In the mid-90s,
the government began its campaign to encourage tourism and declared in 1998 to be "Visit Laos Year." Despite their efforts, there were still
more monks than tourists in the country. Today, Laos is still a poor country but is rapidly developing and poverty declining.
Tourism is the leading industry in the country and the number of tourists continue to rise each year. 
</p>

